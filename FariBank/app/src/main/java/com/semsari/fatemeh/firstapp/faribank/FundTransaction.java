package com.semsari.fatemeh.firstapp.faribank;

public class FundTransaction {

    double amount;
    String type;

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public void setType(String type) {
        this.type = type;
    }

    public double getAmount() {
        return amount;
    }

    public String getType() {
        return type;
    }
}
